          }
        },
        methods:{
          initEvent(){
            var This = this;

            //2. 百叶窗
            $('.sbox-item-2').click(function(){
              This.setMaskId(This.id);
              This.motion($(this),$(this).find('.svgRect'),$(this).find('.cp-img'));
            }); 
            
          },
          motion(wrapper,elem,img){
           var This = this,h = 40;
           img.attr('xlink:href',this.cimg);
           wrapper.addClass('sbox-item-active');
           cancelAnimationFrame(this.timer);
           
           fn();

           function fn(){
            
            This.timer = requestAnimationFrame(fn);
            This.$emit('getTimer',This.timer);
            h-=0.3;
            //h = parseInt(h);

            elem.each(function(index,ele){
              ele.setAttribute('height',h);
            });

            if(h < 0.5){
              h = 40;
            }

           }
          },
          cancelStatus(){
            cancelAnimationFrame(this.timer);
            $('.sbox-item-2').removeClass('sbox-item-active');
            $('.sbox-item-2').find('.svgRect').each(function(index,ele){
              ele.setAttribute('height',40);
            });
          },
          ...mapMutations({
            setMaskId: 'maskid'
          })
        },
        computed:{
          ...mapGetters({
            getMaskId: 'maskid'
          })
        },
        watch:{
          getMaskId(nv,ov){
            if(nv !== this.id){
              this.cancelStatus();
            }
          }
        },
        mounted(){
          this.initEvent();
        }
    }
